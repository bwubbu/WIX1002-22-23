package Tryouts;

/*
arrays of arrays where each sub-array can have a different number of elements.
 */
public class RaggedArrays {
    public static void main(String[] args) {
        int[][] ragged = new int[3][];
        ragged[0] = new int[4];
        ragged[1] = new int[2];
        ragged[2] = new int[3];
    }
}
